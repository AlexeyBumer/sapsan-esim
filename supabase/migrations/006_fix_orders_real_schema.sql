-- Sapsan eSIM — исправление реальной схемы orders (26 июня 2026)
--
-- Выяснилось: миграция 002_orders_admin_sessions.sql была
-- РЕКОНСТРУКЦИЕЙ по описанию из старых чатов, а не точной копией
-- того, что реально выполнялось в Supabase при создании таблицы.
-- Из-за этого живая таблица orders не совпадала с кодом:
--   - не было колонок mode и payer_email (код в /api/checkout
--     и /api/heleket-webhook писал и читал именно их),
--   - user_id был NOT NULL, что делало гостевые заказы
--     (user_id = null) физически невозможными.
--
-- Эта миграция приводит живую таблицу в соответствие с кодом.
-- Идемпотентна — безопасно выполнять повторно.
-- ============================================================

alter table public.orders
  add column if not exists mode text not null default 'new';

alter table public.orders
  add column if not exists payer_email text;

alter table public.orders
  alter column user_id drop not null;

